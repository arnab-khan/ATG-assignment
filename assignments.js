function createAccount() {
    document.getElementsByClassName("create_account")[0].style.display="block";
    document.getElementsByClassName("form_close")[0].style.display="block";
    document.getElementsByClassName("black_background")[0].style.display="block";
}
function formClose() {
    document.getElementsByClassName("create_account")[0].style.display="none";
    document.getElementsByClassName("form_close")[0].style.display="none";
    document.getElementsByClassName("black_background")[0].style.display="none";
}